export function pickVideoPending(ctx: BotContext): string {
  if (ctx.user?.language === "ru") {
    return VIDEO_PENDING_RU[Math.floor(Math.random() * VIDEO_PENDING_RU.length)];
  }
  return ctx.t.video.asyncPending;
}

export function pickDesignPending(ctx: BotContext): string {
  if (ctx.user?.language === "ru") {
    return DESIGN_PENDING_RU[Math.floor(Math.random() * DESIGN_PENDING_RU.length)];
  }
  return ctx.t.design.asyncPending;
}
